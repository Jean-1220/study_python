# 입출력_다양한 출력포맷

#빈자리는 빈공간/오른쪽 정렬/10자리 확보
print("{0: >10}".format(500))

#양수일때 +/음수일땐 -
print("{0: >+10}".format(500))
print("{0: >+10}".format(-500))

#왼쪽 정렬/빈칸은_로 채움
print("{0:_<+10}".format(500))

#3자리마다 콤마를 찍어주기
print("{0: ,}".format(1000000000))

#3자리마다 콤머 / +-부호도
print("{0:+,}".format(100000000000))
print("{0:+,}".format(-100000000000))

#3자리마다 콤마 / 부호 / 자릿수 확보
print("{0:^<+30,}".format(100000000000))

#소수점 출력
print("{0:f}".format(5/3))

#소수점 세번째 자리에서 반올림출력
print("{0:2f}".format(5/3))